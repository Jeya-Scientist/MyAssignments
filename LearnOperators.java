package week1.day1;

public class LearnOperators {
public static void main(String[] args) {
	int a= 20;
	int b=10;
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a==b);
//Relational Operator
	System.out.println(a>b && a<b);

	System.out.println(a>b || a<b);


}
}
